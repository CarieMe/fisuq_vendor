class SetPath {
  static setSvgPath(String data) {
    return 'assets/images/SVG/$data.svg';
  }

  static setPngPath(String data) {
    return 'assets/images/PNG/$data.png';
  }

  static setLottiePath(String data) {
    return 'assets/animation/$data.json';
  }

  static setMp3(String data) {
    return 'assets/sounds/mp3/$data.mp3';
  }

  static setM4r(String data) {
    return 'assets/sounds/m4r/$data.m4r';
  }

  static setWav(String data) {
    return 'assets/sounds/wav/$data.wav';
  }
}
